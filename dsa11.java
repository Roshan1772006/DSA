public class dsa11 {
    public static void main(String[] args) {
        // x++ post increment
        // pehle use karo then increase
        int x = 10;
        System.out.println(x++);
        System.out.println(x);

        //++x pre increment
        // pehle increase then use
        System.out.println(++x);
        System.out.println(x);

        //x-- post decrement
        // pehle use karo then decrease
        System.out.println(x--);
        System.out.println(x);

        //--x pre decrement
        // pehle decrease then use
        System.out.println(--x);
        System.out.println(x);


    }
}
