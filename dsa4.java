public class dsa4 {
    public static void main(String[] args) {
        // Area of circle kya hota hai? pi*r*r

        double pi  = 3.14;
        int r = 9;
        System.out.println(pi*r*r);

        // volume of sphere

        System.out.println((4/3)*pi*r*r*r);

        // tsa of cuboid
        int l = 10 , b = 5 , h = 10;
        int a = 2*(l*h+l*b+h*b);
        System.out.println(a);
    }

}
