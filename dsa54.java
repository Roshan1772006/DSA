import java.util.Scanner;

public class dsa54 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter a number: ");
        int a = sc.nextInt();
        System.out.println("Enter the pow raise number: ");
        int b = sc.nextInt();
        int pow=1;
        for(int i=1; i<=b; i++){
            pow*=a;
        }
        System.out.println(pow);
    }
}
